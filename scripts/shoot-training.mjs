import { chromium } from 'playwright';
import fs from 'fs';
fs.mkdirSync('tmp/shots', { recursive: true });
const B = process.env.SHOOT_BASE || 'http://localhost:5311';
const browser = await chromium.launch();
const ctx = await browser.newContext({ viewport: { width: 1440, height: 1100 } });
await ctx.addInitScript(() => { try { localStorage.setItem('rally_access', 'granted'); sessionStorage.setItem('rally_launched', '1'); } catch {} });
const page = await ctx.newPage();
const errs = [];
page.on('pageerror', e => errs.push('PAGEERR ' + e.message));
page.on('console', m => { if (m.type() === 'error') errs.push('CONSOLE ' + m.text()); });
await page.goto(B + '/training', { waitUntil: 'networkidle', timeout: 45000 });
await page.waitForTimeout(1800);
const crash = await page.getByText('Something went sideways').count();
const deck = await page.locator('.adk').count();
const modules = await page.locator('.tn-steps').count();
const bar = await page.locator('.tn-bar').count();
await page.screenshot({ path: 'tmp/shots/training.png' });
// trigger training badge + a spotlight
await page.evaluate(() => { window.dispatchEvent(new CustomEvent('rally:training', { detail: { on: true } })); });
await page.waitForTimeout(400);
const badge = await page.locator('.tm-badge').count();
await page.evaluate(() => { window.dispatchEvent(new CustomEvent('rally:highlight', { detail: { selector: '.tn-progress', label: 'This is your required path' } })); });
await page.waitForTimeout(700);
const ring = await page.locator('.tm-spot-ring').count();
await page.screenshot({ path: 'tmp/shots/training-spotlight.png' });
const rel = errs.filter(e => !/CSP|upgrade-insecure|favicon|manifest|Download the React|404|realtime/i.test(e));
console.log('crash=' + crash, 'deck=' + deck, 'moduleGroups=' + modules, 'progressBar=' + bar, 'badge=' + badge, 'spotlightRing=' + ring, 'errors=' + rel.length, rel.slice(0, 3).join(' | '));
await browser.close();
